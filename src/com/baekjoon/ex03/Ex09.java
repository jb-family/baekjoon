package com.baekjoon.ex03;

import java.util.Scanner;

public class Ex09 {
//2438번 문제
	public static void main(String[] args) {

		
		Scanner sc = new Scanner(System.in);
		
		int count = sc.nextInt();
		String str = "";
		for(int i = 0; i < count; i++) {
			System.out.println(str += "*");
			
		}
		
		
		
		
		
		
		
		
		sc.close();
	}

}
