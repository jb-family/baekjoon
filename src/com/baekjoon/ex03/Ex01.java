package com.baekjoon.ex03;

import java.util.Scanner;

public class Ex01 {
//2739번 문제
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		
		int num1 = sc.nextInt();
		
		for(int i = 1; i < 10; i++) {	//i는 0부터 8까지 9번 반복 
			
			System.out.println( num1 + " * " + i + " = " + (num1 * i) );
			
			
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		sc.close();
	}

}
 